package org.freakz.pmud.common.player;

public class Pronouns {

    private String me = "";
    private String it = "";
    private String him = "";
    private String her = "";
    private String them = "";

    public String getMe() {
        return me;
    }

    public void setMe(String me) {
        this.me = me;
    }

    public String getIt() {
        return it;
    }

    public void setIt(String it) {
        this.it = it;
    }

    public String getHim() {
        return him;
    }

    public void setHim(String him) {
        this.him = him;
    }

    public String getHer() {
        return her;
    }

    public void setHer(String her) {
        this.her = her;
    }

    public String getThem() {
        return them;
    }

    public void setThem(String them) {
        this.them = them;
    }


}
